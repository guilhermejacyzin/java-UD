import javax.swing.*;

public class Au15Variables {
    public static void main(String[] args) {

        int myFirstNumber = 5;


        System.out.println(myFirstNumber);

        System.out.println(myFirstNumber);

        String myFirstString = "Hello";

        System.out.println(myFirstString);

        myFirstNumber = 10;

        System.out.println(myFirstNumber);

        myFirstNumber = 1000;

        System.out.println(myFirstNumber);

        myFirstNumber = 10 + 5;

        System.out.println(myFirstNumber);

        myFirstNumber = (10 + 5) + (2 * 10);

        System.out.println(myFirstNumber);

    }
}
